# Deliberately NOT Set-StrictMode -- same reasoning as PfbSpecTools.ps1/PfbValueEnumTools.ps1.

# Endpoints reached exclusively through hand-written auth code that never calls
# Invoke-PfbApiRequest (it's what establishes the session Invoke-PfbApiRequest itself
# depends on) -- confirmed by reading Public/Connection/Connect-PfbArray.ps1,
# Public/Connection/Disconnect-PfbArray.ps1, and Private/Invoke-PfbOAuth2Login.ps1
# directly. admins/api-tokens is deliberately NOT here: Connect-PfbArray.ps1 also touches
# it via raw Invoke-RestMethod for post-login token minting, but GET/POST/DELETE
# admins/api-tokens are already covered through the standard Invoke-PfbApiRequest
# convention by Public/Admin/{Get,New,Remove}-PfbApiToken.ps1, so Get-PfbModuleCalledEndpoints's
# normal scan already sees them without needing this allowlist.
$script:PfbBespokeAuthEndpoints = @(
    'GET /api/api_version',
    'POST /api/login',
    'POST /api/logout',
    'POST /oauth2/1.0/token'
)

# Fields never worth reporting as a category-2 gap, each for its own confirmed reason --
# not the same list as $script:PfbBespokeAuthEndpoints (that's endpoint keys; this is
# field names). Deliberately excludes context_names/allow_errors/sort/total_only -- those
# change request *behavior* (fleet-context targeting, bulk-error tolerance, sorting,
# aggregate-only responses) and are real, if broad, gaps, not noise.
#   - X-Request-ID: no functional effect on the request -- request/response-log
#     correlation only ("Supplied by client during request or generated by server," per
#     the spec), present on 629/632 capability-map endpoints as of 2.27 with zero cmdlets
#     exposing it anywhere (confirmed via grep of Public/+Private/).
#   - continuation_token: superseded by Invoke-PfbApiRequest's own -AutoPaginate, which
#     already reads response.continuation_token and re-queries automatically
#     (Private/Invoke-PfbApiRequest.ps1) -- a typed parameter would let a caller interfere
#     with pagination state the wrapper already owns end-to-end.
#   - offset: an alternate, unused positioning mechanism into the same paginated
#     collections -AutoPaginate already retrieves in full; co-occurs with
#     continuation_token on 208/212 of its capability-map endpoints, and zero cmdlets
#     expose it.
$script:PfbNonActionableParameters = @(
    'X-Request-ID',
    'continuation_token',
    'offset'
)

